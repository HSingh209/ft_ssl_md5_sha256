#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[a] != '\0')
	{
		s1[i++] = s2[a++];
	}
	s1[i] = '\0';
	return (s1);
}
